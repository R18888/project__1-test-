// Исходный словарь с именами людей и их городами проживания
const peopleCities = {
    'Алексей': 'Москва',
    'Мария': 'Санкт-Петербург',
    'Иван': 'Москва',
    'Ольга': 'Казань',
    'Дмитрий': 'Санкт-Петербург',
    'Анна': 'Казань'
};

// Функция для преобразования словаря в новый формат, где ключи — города, а значения — списки имен
function groupPeopleByCity(peopleCities) {
    const citiesPeople = {};  // Создаем пустой объект для хранения результата
    
    // Проходим по каждому элементу исходного словаря
    for (const [name, city] of Object.entries(peopleCities)) {
        // Если город уже существует, добавляем имя, создавая новый массив с помощью оператора распространения
        // Если не существует, создаем массив с единственным именем
        citiesPeople[city] = citiesPeople[city] 
            ? [...citiesPeople[city], name] 
            : [name];
    }
    
    return citiesPeople;  // Возвращаем преобразованный словарь
}

// Пример использования функции
const groupedByCity = groupPeopleByCity(peopleCities);
console.log(groupedByCity);
// Вывод: { 'Москва': ['Алексей', 'Иван'], 'Санкт-Петербург': ['Мария', 'Дмитрий'], 'Казань': ['Ольга', 'Анна'] }
