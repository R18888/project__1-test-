function evaluateExpression(a) {
    let result;  // Объявляем переменную для хранения результата вычисления

    // Первый уровень тернарного оператора
    // Проверяем, больше ли `a` 50
    if (a > 50) {
        // Если `a > 50`, проверяем еще раз, больше ли `a` 45
        if (a > 45) {
            // Если `a > 45`, результат равен выражению (6 * a) + 5
            result = (6 * a) + 5;
        } else {
            // Иначе (если a <= 45) проверяем дополнительное условие: меньше ли `a` 11
            result = (a < 11) ? 5 : 6 * (a - 6);  // Если `a < 11`, результат равен 5, иначе — результат равен 6 * (a - 6)
        }
    } else {
        // Если `a <= 50`, проверяем, больше ли произведение `a * 6` числа 45
        if (a * 6 > 45) {
            // Если произведение больше 45, результат равен (6 * a) + 5
            result = (6 * a) + 5;
        } else {
            // Если произведение не больше 45, проверяем условие: меньше ли `a` 11
            result = (a < 11) ? 5 : 6 * (a - 6);  // Если `a < 11`, результат равен 5, иначе результат равен 6 * (a - 6)
        }
    }

    console.log(result);  // Выводим результат в консоль
    return result;  // Возвращаем результат из функции
}

// Пример использования функции
evaluateExpression(55);  // Вызов функции с аргументом a = 55, выводит результат для этого значения
evaluateExpression(8);   // Вызов функции с аргументом a = 8, выводит результат для этого значения
evaluateExpression(20);  // Вызов функции с аргументом a = 20, выводит результат для этого значения
